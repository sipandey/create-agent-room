test('parseSafeJSON: parses standard JSON', () => {
  const json = '{"a": 1, "b": "hello"}';
  const result = parseSafeJSON(json);
  assert.deepStrictEqual(result, { a: 1, b: 'hello' });
});

test('parseSafeJSON: strips single line comments', () => {
  const json = `{
    // This is a comment
    "a": 1,
    "b": "http://google.com" // another comment
  }`;
  const result = parseSafeJSON(json);
  assert.deepStrictEqual(result, { a: 1, b: 'http://google.com' });
});

test('parseSafeJSON: strips multi line comments', () => {
  const json = `{
    /* This is a
       multi-line comment */
    "a": 1
  }`;
  const result = parseSafeJSON(json);
  assert.deepStrictEqual(result, { a: 1 });
});

test('parseSafeJSON: handles empty or invalid json safely', () => {
  assert.deepStrictEqual(parseSafeJSON(''), {});
  assert.deepStrictEqual(parseSafeJSON('   '), {});
  assert.throws(() => parseSafeJSON('{"a": }'), SyntaxError);
});

