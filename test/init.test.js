async function captureConsoleLog(asyncFn) {
  const lines = [];
  const original = console.log;
  console.log = (...args) => lines.push(args.join(' '));
  try {
    await asyncFn();
  } finally {
    console.log = original;
  }
  return lines.join('\n');
}

